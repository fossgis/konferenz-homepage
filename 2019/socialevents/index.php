            <h2>Social Events</h2>

            <h3>Dienstag, 12. März 2019</h3>
                
            <h4>Innoffizieller Start</h4>
           
            <p>Am Abend vor der FOSSGIS-Konferenz besteht die Möglichkeit bei einem gemeinsamen Abendessen alte Gesichter wiederzusehen und neue Bekanntschaften zu machen. Für den inoffiziellen Start wurde am 12. März 2019 ab 18:30 Uhr eine offene Reservierung im <a href="https://www.altmarktkeller.de/speisen-getraenke/">Altmarktkeller Dresden</a>, Adresse: <a href="https://www.openstreetmap.org/export#map=18/51.04962/13.73932">Altmarkt 4</a>, 01067 Dresden eingerichtet.<br>Die Küche bietet bis ca. 23 Uhr warme Speisen. Jeder bezahlt seine Rechnung selbst.</p>

<p> Wer dann noch weitergehen möchte, hat die Möglichkeit die umliegende <a href="https://www.openstreetmap.org/export#map=17/51.04948/13.74082">Weiße Gasse</a>, oder aber die <a href="https://www.openstreetmap.org/export#map=15/51.0650/13.7569">Dresdner Neustadt</a> zu erkunden. </P>

             <h3>Mittwoch, 13. März 2019</h3>
		 <h4>Eröffnung FOSSGIS-Konferenz</h4>
		 <p>um 13:00 Uhr im Audimax der HTW Dresden, Friedrich-List-Platz 1, Dresden.</p>

 <h4>Anwendertreffen</h4>
         <p>Es können an allen Tagen Anwendertreffen, Sprints und BOFs selber organisiert werden. Räume stehen bis 19:30 Uhr zur Verfügung, bitte <a href="mailto:konferenz-orga@fossgis.de">per E-Mail</a>  an das Konferenzteam anfragen oder mit dem Team vor Ort ansprechen.</p>

<h4>Abendveranstaltung "Dialoge im Bärenzwinger"</h4>
		 <p>Das gesellige Beisammensein in Form der Abendveranstaltung "Dialoge im Bärenzwinger" findet von 19 - 23 Uhr statt. Das Buffet wird im angrenzenden historische Kassemattengewölbe eröffnet. Der <a href="https://www.baerenzwinger.de/" target="_blank" rel="nofollow">Bärenzwinger</a> ist ein Studentenclub und liegt direkt an der historischen Altstadt, den Brühlschen Terrassen. Adresse: Studentenclub Bärenzwinger, <a href="https://www.openstreetmap.org/node/267966087">Brühlscher Garten 1</a>, 01067 Dresden. Per Straßenbahnlinie 3, 7, 8 und 9 erreichbar. Zu Fuß ist man vom Zentralgebäude der HTW entlang der Einkaufsstraße "Prager Straße" ca. 30 Minuten (ohne Einkauf) unterwegs. 
<br>  		<p>Zur Veranstaltung werden jedem Gast Getränkegutscheine gereicht, die an der Bar des Bärenzwingers eingelöst werden können.
            </p>
               
            <p>Der Eintritt ist im FOSSGIS-Konferenz-Ticket enthalten. Die Teilnehmer*innenzahl ist auf 250 Personen begrenzt. Da ein Zutritt nur mit Ticket möglich sein wird, ist eine verbindliche Voranmeldung bei der Konferenz-Ticketbuchung ratsam. 
            </p>
          

            <h3>Donnerstag, 14. März 2019</h3>

			<h4>Fototermin </H4>
			<p>Alle Konferenzteilnehmer und Teilnehmerinnen sind zu 15:00 Uhr gebeten sich zu einem gemeinsamen Gruppenfoto an der Treppe vor dem Haupteingang der Hochschule (Z-Gebäude) einzufinden. </p>
		 	<h4>FOSSGIS e.V. Mitgliederversammlung</h4>
            
           	<p>ab 18:00 Uhr im Mathe-Hörsaal Z211. Alle Mitglieder sind herzlich eingeladen, teilzunehmen und sich zu beteiligen. Der FOSSGIS-Verein lädt ein zum Diskutieren, Kennenlernen, Abstimmen & Neuwahlen.
Getränke werden gestellt und es wird für alle Pizza bestellt. Der Verein freut sich über zahlreiches Erscheinen.
</p>

            <h3>Freitag, 15. März 2019</h3>
		 	<h4>Sektempfang am FOSSGIS-Stand </h4>
			<p>Alle Mitglieder des FOSSGIS-Vereins, Freunde und Interessierte sind ab 14:30 Uhr herzlich zum Sektausklang eingeladen.</p>

			<h4>OSM-Event am Freitagnachmittag</h4>
			<p>Das Programmkomitee hat ab für Freitagnachmittag ein Vortragsprogramm mit OpenStreetMap-Themen zusammengestellt. Es beginnt um 15 Uhr mit dem OSM-Quiz.</p> 

            <h4>Exkursion zum Mathematisch-Physikalischen Salon  im Zwinger</h4>
            <p>Um 15:30 Uhr ist am Freitagnachmittag eine Exkursion zum <a href="https://mathematisch-physikalischer-salon.skd.museum" target="_blank" rel="nofollow">Mathematisch-Physikalischen Salon im Zwinger</a> geplant.<br>
            Es wird eine auf die Konferenzteilnehmer zugeschnittene Führung durch das Museum geben. Der Eintritt zum Salon ist durch jeden Teilnehmer selbst zu zahlen.Interessierte sind herzlich eingeladen sich anzuschließen. <br> Bitte geben sie ihre Teilnahme im Anmeldesystem an, dies dient der guten Vorbereitung.</p>

            <h4>Anwendertreffen</h4>
         	<p>Es können an allen Tagen Anwendertreffen, Sprints und BOFs selber organisiert werden. Räume stehen bis 19:30 Uhr zur Verfügung, bitte <a href="mailto:konferenz-orga@fossgis.de">per E-Mail</a>  an das Konferenzteam anfragen oder mit dem Team vor Ort ansprechen.</p>

			<h3>Samstag, 16. März 2019</h3>
		 	<h4>OSM-Samstag </h4>
			<p>Von 9:30 bis 18 Uhr findet die mittlerweile beliebte OSM-Unconference statt. Interessierte sind eingeladen daran teilzunehmen. Die Veranstaltung ist kostenfrei. Die Themensammlung erfolgt im <a href="https://wiki.openstreetmap.org/wiki/FOSSGIS_2019/OSM-Samstag"> OSM-Wiki </a> sowie an der Pinnwand am FOSSGIS-Stand. Um 10 Uhr werden die Themen in Zeiten und Räume sortiert. Für das leibliche Wohl wird gesorgt sein. Wir bitten um Anmeldung über das <a href="https://www.fossgis-konferenz.de/2019/anmeldung/">FOSSGIS-Konferenz-Teilnehmeranmeldesystem</a></p>

