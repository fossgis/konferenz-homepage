                <h2>Call for Papers</h2>
				<p>Sie forschen, Sie entwickeln, Sie beschäftigen sich mit Open-Source-Geo-Software oder freien Geodaten? Sie haben neue Ideen in diesem Themenbereich oder sind bereits dabei, freie Software und freie Geodaten zu nutzen oder zu entwickeln? Dann sind Sie auf unserer Konferenz - der FOSSGIS-Konferenz 2014 - genau richtig.</p>

                <h3>Wir bieten</h3>
                <p>Eine Plattform für Ihre Idee, Ihr Projekt, Ihren Erfahrungsbericht auf der größten deutschsprachigen Anwenderkonferenz für freie Geoinformationssysteme und freie Geodaten. Im Jahr 2014 findet die FOSSGIS vom 19. bis 21. März auf dem Gelände der Beuth Hochschule für Technik in Berlin statt. Wir rechnen mit über 400 Teilnehmern. An der Konferenz 2013 in Rapperswil (Schweiz) nahmen über ca. 350 Besucher teil.</p>

                <h3>Wir suchen</h3>
                <p>Ihre Idee. Ihr Projekt. Ihren Erfahrungsbericht. Ihr Thema. Genauer gesagt, suchen wir Vorträge für Einsteiger und Fortgeschrittene, um spannende Themen zu behandeln, Diskussionen zu entwickeln, praxisorientierte Workshops runden unser Programm ab. Vorträge zum Thema freie Geodaten, zum Beispiel OpenStreetMap, Open Data sind ebenso möglich wie Beiträge zu beispielsweise Softwarelösungen aus dem Bereich WebGIS, Desktop GIS, Geodatenbanken oder Location-Based-Services. Bewerben Sie sich jetzt mit einem Vortrag, Lightning Talk oder Workshop* beim Call for Papers.</p>

                <h3>Abstract</h3>
                <p>Die Einreichung eines Abstracts für die FOSSGIS 2014 ist ab sofort bis zum 15. November 2013 über <a href="http://pb.fossgis.de/submission/FOSSGIS2014/">unsere Konferenzsoftware Pentabarf</a> möglich. Beitrags-Einreichungen ohne Abstract müssen leider abgelehnt werden, da sich das Programm-Komitee anhand des Abstracts ein Bild von Inhalt und Relevanz des Beitrags macht. Der Abstract sollte ca. 1500 Zeichen umfassen. Ob die Einreichung angenommen wurde, wird im Dezember 2013 bekanntgegeben. Es besteht die Möglichkeit einen Lightning Talk einzureichen. Ein Lightning Talk ist eine kurze, knackige Auseinandersetzung mit einem Thema, welches in 5 Minuten, gern humorvoll, Anregung zur Diskussion bietet. Weiterhin können Poster eingereicht werden.</p>

                <h3>Workshops</h3>
                <p>Bitte berücksichtigen Sie bei der Planung, dass für die Workshops nur 90 Minuten vorgesehen sind und die Teilnehmer ein Mix aus Theorie und vor allem Praxis erwarten. Die Beschreibung des Workshops soll erreichbare Lernziele und die notwendigen Vorkenntnisse der Teilnehmer enthalten. Ein Workshop findet ab einer Teilnehmerzahl von 5 statt.</p>

                <h3>Wer wir sind</h3>
                <p>Veranstaltet wird die Konferenz vom FOSSGIS e.V., von der OSGeo Foundation und der Beuth Hochschule für Technik Berlin.</p>

                <p>Unsere Konferenz lebt von Ihren Beiträgen und Ihrem Besuch. Werden Sie Teil der Konferenz - wir freuen uns auf Sie!</p>

                <p>Noch Fragen? Wir haben versucht, in unseren <a href="faq.<?php  ?>">FAQs</a> alle wichtigen Fragen zu beantworten. Falls weitere Fragen bestehen, zögern Sie nicht uns eine E-Mail zu schreiben.</p>

                <h2>Note to English speakers</h2>

                <p>The FOSSGIS-conference is the largest german-language conference for Free and Open Source Software for GIS and Free Geodata hosting about 400 participants.</p>

                <p>Today, the Conference Committee announced the "Call for papers" for 2014. Because the conference-language is german, the CfP is either.</p>

                <p>The program committee will, however, also consider applications for talks or workshops held in English if they are deeemed to add to the quality of the conference. So if you don't speak German, but are a FOSS/Open Data celebrity, or have a story that only you can tell, please do submit your talk. We are unlikely to be able to provide interpreters, but we'll make sure you don't get lost in Berlin.</p>

                <p>Please be aware that you can submit paper until November, 15. 2013. You can submit your abstract using <a href="http://pb.fossgis.de/submission/FOSSGIS2014/">Pentabarf</a>.</p>
                <p>We are looking forward to see you in Berlin March 19-21.</p>
