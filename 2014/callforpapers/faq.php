                <h2>FAQ</h2>

                <dl>
                	<dt>Ich möchte als eine Vortrag auf der FOSSGIS-Konferenz halten, was muss ich dafür tun?</dt>
                	<dd>Reichen Sie einen Abstract, also eine Kurz-Zusammenfassung mit ca. 1500 Zeichen ein. Dieser wird dann nach der Einreichungsfrist vom Programmkomitee begutachtet und bewertet. Einreichungen die keinen Abstract enthalten werden generell abgelehnt.</dd>

                	<dt>Welche Informationen benötigt das Programmkomitee über mich?</dt>
                	<dd>Wir benötigen Ihren Namen sowie E-Mail-Adresse. Diese Daten werden bei der Anmeldung im Konferenzsystem abgefragt. Bitte beachten Sie, dass Ihr Name durch die Eintragung im Konferenzprogramm öffentlich sichtbar wird.</dd>

                	<dt>Bekomme ich eine E-Mail dass mein Vortrag korrekt eingereicht ist?</dt>
                	<dd>Nein. Der Vortrag wird von Ihnen direkt im Konferenzsystem eingegeben und dort gespeichert (achten Sie darauf den Eintrag zu speichern bevor Sie die Seite verlassen). Somit ist es möglich bis zur Einreichungsfrist Änderungen oder Verbesserungen durchzuführen.</dd>

                	<dt>Wie viel Zeit habe ich für mein Vortrag?</dt>
                	<dd>Ein Vortrag dauert 20 Minuten. Im Anschluss stehen dann noch 5 Minuten Zeit für Fragen zur Verfügung. 3 Vorträge werden jeweils zu einer Session zusammengefasst.</dd>

                	<dt>Nach welchen Kriterien werden die Vorträge ausgewählt?</dt>
                	<dd>Das Programmkomitee entscheidet über verschiedene Auswahlkriterien. So sind Qualität und Vollständigkeit und auch die Relevanz des Themas für das Gesamtprogramm wichtig. Zudem versuchen wir die Wünsche der Besucher aus dem letzten Jahr in die Auswahl einfließen zu lassen. Eine große Rolle spielt auch der Open-Source-Schwerpunkt der FOSSGIS - im Idealfall kann der Zuhörer sich nach einem interessanten Vortrag die besprochene Software im Quelltext herunterladen und selber ausprobieren. Vorträge, die proprietäre Software behandeln, werden nur in Ausnahmefällen angenommen.</dd>

                	<dt>Bis wann kann ich mich mit meinem Vortrag bewerben?</dt>
                	<dd>Ihren Vortrag können Sie bis zum 15.11.2013 einreichen.</dd>

                	<dt>Wie lang oder kurz muss die Einreichung für meinen Vortrag sein?</dt>
                	<dd>Die Beschreibung für Ihren Vortrag sollte ca. 1500 Zeichen haben. Nicht mehr und nicht weniger. Das Programmkomitee muss sich einen Eindruck machen können, worum es geht.</dd>

                	<dt>Wie erfahre ich, ob mein Beitrag angenommen wurde?</dt>
                	<dd>erhalten bis Ende 2013 per E-Mail eine Bestätigung über die Annahme.</dd>

                	<dt>Wann wird das finale Programm veröffentlicht?</dt>
                	<dd>Das finale Programm wird zu Beginn 2014 veröffentlicht, so dass die Planung der Anreise rechtzeitig möglich ist.</dd>

                	<dt>Wann muss mein Vortrag dann fertig sein?</dt>
                	<dd>Spätestens zu Beginn der Vortragssession. Wir benötigen den ausgearbeiteten Vortrag nicht vorher. Auf der Website wird eine Kurzinformation veröffentlicht, daher sollte der Einreichungsabstract möglichst informativ sein. Für den Tagungsband benötigen wir eine ausführlichere Version des Beitrags bis Mitte Januar 2014.</dd>

                	<dt>Wieso muss ich meinen Tagungsband-Beitrag vorher einreichen?</dt>
                	<dd>Der Tagungsband wird gedruckt und allen Teilnehmern zur Verfügung gestellt. Hierfür ist ein Vorlauf von 8 Wochen erforderlich.</dd>

                	<dt>Erhalte ich als Vortragende/r freien Eintritt?</dt>
                	<dd>Natürlich, ausser Sie möchten die Veranstaltung gerne unterstützen und haben entsprechende Finanzmittel.</dd>

                	<dt>Bekomme ich ein Honorar für meinen Votrag?</dt>
                	<dd>Nein.</dd>

                	<dt>Übernimmt FOSSGIS Hotel- oder Fahrtkosten?</dt>
                	<dd>Nein, diese Kosten können wir leider nicht übernehmen.</dd>

                	<dt>Kann ich als Vertreter eines Unternehmens mit einem Vortrag teilnehmen?</dt>
                	<dd>Natürlich können Sie als Vertreter eines Unternehmens am CfP teilnehmen. Jedoch lehnen wir reine Unternehmens- oder Produktpräsentationen in der Regel ebenso ab wie PR-Vorträge.</dd>

                	<dt>Kann ich mein Unternehmen bewerben?</dt>
                	<dd>Selbstverständlich. Hierfür gibt es einen speziellen, gut besuchten Messebereich in der Nähe des Catering. Sie können ausserdem Sponsor werden oder als Medienpartner werben. Entsprechende Informationen finden Sie im Wiki unter: <a href="http://www.fossgis.de/wiki/2014/Sponsoren_und_Aussteller">Sponsoren und Aussteller</a>.</dd>

                	<dt>Wieviele Vorträge darf ich einreichen?</dt>
                	<dd>Es werden maximal 2 Vorträge pro Person gestattet. "Übereinreichungen" werden ohne Rücksprache und ohne Bewertung gelöscht. Bitte überlegen Sie sich deshalb vorher welcher Beitrag Ihnen wirklich wichtig ist.</dd>

                	<dt>Gibt es allgemeine Einführungen zu Open Source ohne Bezug auf spezielle Projekte oder Firmen?</dt>
                	<dd>Ja. Im Rahmen der FOSSGIS 2014 gab es Testweise einen <a href="http://www.fossgis.de/wiki/OS-Block-FOSSGIS-Konferenz_OpenSource_Einf%C3%BChrung">OS-Block-FOSSGIS-Konferenz OpenSource Einführung</a>. Bitte nehmen Sie direkt mit dem Programmkomitee Kontakt auf, wenn Sie diesen Block mit gestalten möchten.
</dd>
