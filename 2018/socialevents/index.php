      <h2>Social Events</h2>
        <h3>Zusammentreffen mit den OSGeo Code Sprintern 2018</h3>
		<p>Zeitgleich zur FOSSGIS findet der internationale <a href="https://wiki.osgeo.org/wiki/OSGeo_Code_Sprint_2018">OSGeo Code Sprint</a> vom 19. bis 25. März 2018 im Bonner BaseCamp statt. Der In- und Outdoor Retro-Park mit ausgefallenen Wohnwagen und Zugschlafwagen hat sich mit seinem nostalgischen Flair schon bei der FOSS4G 2016 bewährt.
		<br> Wer Lust hat internationale Entwickler unserer OSGeo-Projekte zu treffen, beteiligt sich am "Pubrace". Beim seit der FOSS4G 2016 beliebten Pubrace treffen sich Leute Abends ab 20 Uhr bei einem Getränk im <a href="http://www.openstreetmap.org/node/1581262116">Namenlos Bonn</a> am Tor zur Bonner Altstadt oder im <a href="http://www.openstreetmap.org/node/225642197">Pawlow</a> im Herzen derselbigen.
		    <br>Wer Lust hat direkt auch am OSGeo Code Sprint teilzunehmen ist ebenso herzlich eingeladen. Anmeldung unter <a href="https://wiki.osgeo.org/wiki/OSGeo_Code_Sprint_2018">OSGeo Code Sprint</a>.
		    </p>

		

		 <h3>Dienstag, 20. März 2018</h3>
         <p>Ab 19:00 Uhr Inoffizieller Start bei gemeinsamen Abendessen für alle schon anwesenden FOSSGIS'ler im <a href="https://www.gesindehaus-bonn.de/">Gesindehaus in Poppelsdorf</a> in der <a href="https://www.openstreetmap.org/way/30070888">Clemens-August Straße 59</a>. Jeder bezahlt seine Rechnung selbst. </p>

        <h3>Mittwoch, 21. März 2018</h3>
		 <h4>Eröffnung FOSSGIS-Konferenz</h4>
				<p>um 13:00 Uhr im Wolfgang-Paul-Hörsaal, Kreuzbergweg 28, Bonn.</p>

		 <h4>Anwendertreffen</h4>
          <p>Es können an allen Tagen Anwendertreffen, Sprints und BOFs selber organisiert werden. Räume stehen bis 19:30 Uhr zur Verfügung, bitte <a href="mailto:konferenz-orga@fossgis.de">per E-Mail</a>  an das Konferenzteam anfragen oder mit dem Team vor Ort ansprechen.</p>

		<h4>Abendveranstaltung "Campus-Dialoge"</h4>
		 <p>Das gesellige Beisammensein findet von 19:15 - 0:30 Uhr in der CAMPO Campusmensa Poppelsdorf der Universität Bonn statt. Die "Campus-Dialoge" werden sich durch ein offenes Ambiente auszeichnen.
Neben einem reichhaltigen Rheinischenm Buffet und Getränken bieten wir Raum zur Diskussion und zur späteren Stunde auch ein kulturelles Programm. 
<br>
<a href="https://de-de.facebook.com/dadsphonkey/" target="_blank" title="Dad's Phonkey">Dad's Phonkey - Christian Padberg</a> singt mit sich selbst und entfacht mit seiner Loop-Station ein musikalisches Feuerwerk. Anschließend wird Rafa vom <a href="http://luchaamada.blogsport.de/" target="_blank" title="DJ Kollektiv Lucha Amada">Lucha Amada DJ Kollektiv</a> für Stimmung sorgen.
<br>
Der Eintritt ist im FOSSGIS-Konferenz-Ticket enthalten. Wir bitten darum bei der Anmeldung die Teilnahme an der Abendveranstaltung anzugeben, dies dient der guten Planung der Veranstaltung. 
<br>
Darüber hinaus kann das Social Event auch separat ohne Konferenzteilnahme gebucht werden - <a href="https://pretix.eu/fossgis/konf-2018/w/Tg5PUTaoTJOIAaoY/redeem?voucher=CAMPUS_DIALOG_3QMZX9X7MPWYDF8K&subevent=&locale=de" target="_blank" title="Buchung Social Event">zur Buchung</a>.
</p>

		<h3>Donnerstag, 22. März 2018</h3>
		 <h4>FOSSGIS e.V. Mitgliederversammlung</h4>
			<p>ab 18:00 Uhr im Alfred-Philippson-Hörsaal. Alle Mitglieder sind herzlich eingeladen, teilzunehmen und sich zu beteiligen. Einige Themen stehen auf der Agenda. Wir laden ein zum Kennenlernen, zur Diskussion, Abstimmung & Neuwahlen.
Getränke werden gestellt und es wird für alle Pizza bestellt. Wir freuen uns über zahlreiches Erscheinen.
</p>

		<h3>Freitag, 23. März 2018</h3>
				<h4>Sektempfang am FOSSGIS-Stand </h4>
				<p>Alle Mitglieder des FOSSGIS-Vereins, Freunde und Interessierte sind ab 16:30 Uhr herzlich zum Sektausklang eingeladen.</p>

		<h3>Samstag, 24. März 2018</h3>
				<h4>OSM-Samstag </h4>
				<p>Von 9:30 bis 18 Uhr findet die mittlerweile beliebte OSM-Unconference statt. Interessierte sind eingeladen daran teilzunehmen. Die Themensammlung erfolgt im <a href="https://wiki.openstreetmap.org/wiki/FOSSGIS_2018/OSM-Samstag"> OSM-Wiki </a> sowie an der Pinnwand am FOSSGIS-Stand. Um 10 Uhr werden die Themen in Zeiten und Räume sortiert. Für das leibliche Wohl wird gesorgt sein. Wir bitten um Anmeldung über das <a href="https://www.fossgis-konferenz.de/2018/anmeldung/" >FOSSGIS-Konferenz-Teilnehmeranmeldesystem</a></p>

