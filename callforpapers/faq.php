					<h2>Call for Papers - FAQ</h2>
					<dl>
                        <dt>Ich möchte als eine Vortrag auf der FOSSGIS-Konfernez halten, was muss ich dafür tun?</dt>
                        <dd>Reiche einen Abstract, also eine Kurz-Zusammenfassung mit max. 1500 Zeichen ein. Dieser wird dann nach der Einreichungsfrist vom Programmkomitee begutachtet und bewertet.</dd>

                        <dt>Welche Informationen benötigt ihr über mich?</dt>
                        <dd>Wir benötigen deinen Namen und deine E-Mail-Adresse. Das alles kannst du bequem im <a href="http://pb.fossgis.de/submission/FOSSGIS2013/">Pantabarf</a> eintragen.</dd>

                        <dt>Bekomme ich eine E-Mail dass mein Vortrag korrekt eingereicht ist?</dt>
                        <dd>Nein der Vortrag landet direkt nach dem Speichern in unserem System. So kannst du auch noch bis zur Einreichungsfrist Änderungen oder Verbesserungen durchführen.</dd>

                        <dt>Wie viel Zeit habe ich für mein Vortrag?</dt>
                        <dd>Ein Vortrag dauert 20 Minuten. Im Anschluss stehen dann noch 5 Minuten Zeit für Fragen zur Verfügung. 3 Vorträge werden jeweils zu einer Session zusammengefasst.</dd>

                        <dt>Nach welchen Kriterien werden die Vorträge ausgewählt?</dt>
                        <dd>Das Programmkomitee entscheidet über verschiedene Auswahlkriterien. So sind Qualität und Vollständigkeit und auch die Relevanz des Themas für das Gesamtprogramm wichtig. Zudem versuchen wir die Wünsche der Besucher aus dem letzten Jahr mit in die Auswahl einfließen zu lassen. Eine große Rolle spielt auch der Open-Source-Schwerpunkt der FOSSGIS - im Idealfall kann der Zuhörer sich nach einem interessanten Vortrag die besprochene Software im Quelltext herunterladen und selber ausprobieren. Vorträge, die proprietäre Software behandeln, werden nur in Ausnahmefällen angenommen.</dd>

                        <dt>Bis wann kann ich mich mit meinem Vortrag bewerben?</dt>
                        <dd>Deinen Vortrag kannst du bis zum 31.01.2013 einreichen über das <a href="http://pb.fossgis.de/submission/FOSSGIS2013/">Pantabarf</a> einreichen.</dd>

                        <dt>Wie lang oder kurz muss die Einreichung für meinen Vortrag sein?</dt>
                        <dd>Die Beschreibung für deinen Vortrag sollte max. 1500 Zeichen haben. Nicht mehr und nicht weniger. Das Programmkomitee muss sich einen Eindruck machen können worum es geht.</dd>

                        <dt>Wie erfahre ich, ob mein Beitrag angenommen wurde?</dt>
                        <dd>Wir melden uns per E-Mail bei dir bis Mitte Februar.</dd>

                        <dt>Wann wird das finale Programm veröffentlicht?</dt>
                        <dd>Das finale Programm wird Mitte Februar veröffentlicht. So kannst du früh genug deine Anreise planen. </dd>

                        <dt>Wann muss mein Vortrag dann fertig sein?</dt>
                        <dd>Spätestens zu Beginn deiner Session. Den ausgearbeiteten Vortrag benötigen wir vorher nicht. Deine Kurzbeschreibung wird jedoch auf der Website veröffentlicht und sollte daher schon bei Einreichung möglichst informativ sein. Zudem solltest du einen langen Beitrag für den Tagungsband vorher reinreichen.</dd>

                        <dt>Wieso muss ich meinen Tagungsband-Beitrag vorher einreichen?</dt>
                        <dd>Der Tagungsband wird gedruckt und allen Teilnehmer zur Verfügung gestellt. Hierfür ist ein gewisser Vorlauf nötig.</dd>

                        <dt>Erhalte ich als Vortragende/r freien Eintritt?</dt>
                        <dd>Natürlich.</dd>

                        <dt>Bekomme ich ein Honorar für meinen Votrag?</dt>
                        <dd>Nein.  </dd>

                        <dt>Übernehmt ihr Hotel oder Fahrtkosten?</dt>
                        <dd>Nein, auch das können wir leider nicht tun.</dd>

                        <dt>Kann ich als Vertreter eines Unternehmens mit einem Vortrag teilnehmen?</dt>
                        <dd>Natürlich kannst du als Vertreter eines Unternehmens am CfP teilnehmen. Reine Unternehmens- oder Produktpräsentationen lehnen wir jedoch in der Regel ebenso wie PR-Vorträge ab. Falls dein Unternehmen aber Interesse an einer Zusammenarbeit hat z.B. durch Sponsoring, setzt dich einfach mit uns Verbindung.</dd>

