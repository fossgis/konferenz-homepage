	<h2>Unterstützung der Konferenzorganisation</h2>

	<p>Seit vielen Jahren findet die FOSSGIS-Konferenz im deutschsprachigen Raum an wechselnden Standorten statt.<br>
Ohne die Unterstützung durch zahlreiche Mitorganisierende vor der Veranstaltung und Helfer:innen während der Veranstaltung wäre eine FOSSGIS-Konferenz nicht möglich.<br>
	<p>Die Konferenz wird vorwiegend durch ehrenamtliches Engagement getragen. Dies ermöglicht eine flexible Konferenzorganisation und vielfältiges Rahmenprogramm. Die Aufgaben werden auf mehrere Schultern verteilt.</p>

	<p><b>Damit dies so bleiben kann, brauchen wir Deine Unterstützung!<sup>*</sup></b><br>
		<span style="color:grey">* wer in der Community aktiv ist, wird geduzt :-)
         </span></p>
	
	<p>Auf dieser Seite kommen hin und wieder neue Informationen hinzu, also gerne wiederkommen.</p>

	<h3>Wo kannst Du bei der Konferenzorganisation unterstützen?</h3>
	<ul>
		<li><b>Programmkomitee</b><br>
Call for Participation formulieren, Einreichungen reviewen, Programm gestalten, bei Umsetzung des Programms unterstützen.<br>
2022: das Programmkomitee hat sich konstituiert, siehe Wikiseite <a href="https://www.fossgis.de/wiki/Konferenz_2022/Organisationsteam#Programmkomitee"> Programmkomitee</a></li>
		<li><b>Presse- und Öffentlichkeitsarbeit</b><br>
Pressetexte schreiben, Konferenz bekannter machen, raus aus der Blase, neue Menschen erreichen. Teilnehmende informieren.</li>
		<li><b>Helfer:innenkorrdination</b><br>
Engelsystem, Überblick, Helfer:innen anwerben, Jobbeschreibungen, Job-Schichten koordinieren - genau das hier ^ gut zu organisieren ;-)</li>
		<li><b>Technische Umsetzung</b><br>
Unterstützung der technischen Umsetzung der Konferenz in Zusammenarbeit mit dem VOC, u.a. vorab aufgezeichnete Vorträge einsammeln und zur Verfügung stellen, Mails aus Pretalx versenden.</li>
		<li><b>Workshoporganisation</b><br>
Online oder Vor-Ort-Workshop, was braucht es für die gute Umsetzung, Kommunikation mit Workshopleiter:innen, Information der TN.</li>
		<li><b>OSM-Event</b><br>
Das OSM-Event organisieren (Unconference, Mappingevent...) und moderieren.</li>
		<li><b>Programmheft</b><br>
Programm als gedruckte Ausgabe im Format A6 für die TN.</li>
		<li><b>Teilnehmer:innenbetreuung/ Welcome Desk</b><br>
Im Arbeitspaket Teilnehmer:innenbetreeuung stecken mehrere Aufgaben.
Die Informationen zur Konferenz im Vorfeld so aufbereiten, dass es für die TN übersichtlich und nachvollziehbar ist (Website, TN-Mail).
Welcome Desk in Marburg organisieren ist in erster Linie eine Aufgabe des Lokal Teams.
Einen Code of Conduct zu formulieren, steht schon lange auf der Agenda (Beispiel: https://2021.foss4g.org/about/coc.html).</li>
		<li><b>Statistik und Teilnehmer:innenbefragung</b><br>
Onlinebefragung + Auswertung, Befragung zu den Workshops auf den weg bringen.</li>
		<li><b>Hygienekonzept</b><br>
Umsetzungskonzept für die Veranstaltung vor Ort sowie Informationen an Teilnehmende im Vorfeld dr Konferenz (Website, TN-Mail).</li>
		<li><b>Bildungsurlaub</b><br>
Die Konferenz als Bildungsurlaub zu bewerben, kann es für viele Arbeitnehmer:innen attraktiver machen. In 2021 gab es drei positive Bescheide (BER, BB, RP, Zusage von RP gilt zwei Jahre). Es muss für alle Bundesländer ein Antrag gestellt werden. Im ersten Schritt die kostenfreien, bei entsprechendem Bedarf können auch die kostenpflichtigen beantragt werden.
TN bekommen im Ticketsystem die Frage, ob sie Bildungsurlaub in Anspruch nehmen möchten, es wird nach Bundesland gefragt. Am Ende muss es gegenüber dem genehmigenden Bundesland abgerechnet werden.</li>
	</ul>
	<p>Im Wikihandbuch ist die Organisation der Konferenz einmal komplett beschrieben: <a href="https://www.fossgis.de/wiki/Handbuch">https://www.fossgis.de/wiki/Handbuch</a>

	<h3>Wo kannst Du bei der Durchführung der Konferenz unterstützen?</h3>
	<p>Es gibt zwei Helfer:innenarten.</p>
	
	<ul>
		<li><b>Freiwillige Helfer </b>(ab 6h helfen gibt es ein kostenfreies Helfer:innenticket)<br>
Die Konferenz wird vorwiegend durch ehrenamtliches Engagement getragen.</li>
		<li><b>Bezahlte Helfer </b>(Aufwandsentschädigung über FOSSGIS e.V.)<br>
Für Aufgaben, die während der Umsetzung unbedingt gemacht werden müssen, steht ein Budget zur Aufwandsentschädigung bereit.</li>
	</ul>
	<p>Es gibt folgende Helfder:innenaufgaben, die während der Konferenz zu erledigen sind. Die Schichten werden über das <a href="">Helfersystem</a> gebucht.</p>

	<h4>Helfen vor Ort in Marburg</h4>
	<ul>
		<li><b>Sessionleitung</b> (freiwillig)<br>
Die Sessionleitung moderiert den jeweiligen Track, kündigt Vortrag und Vortragende an und gibt organisatorische Informationen an die Teilnehmenden. Sessionleitung ist vor Ort und moderiert aus dem Hörsaal.
			<ul><i>Aufgaben:</i>
				<li>Anmoderation des Tracks</li>
				<li>mitlesen des Online-Chats während des Vortrags</li>
				<li>relevante Fragen sammeln, dabei eventuell vorhandenes Voting der TN beachten</li>
				<li>ausgewählte Fragen an Vortragende stellen.</li>
				<li>(Matrix-Chat im Blick behalten )</li>
				<i>technische Ausstattung für Online-Sessionleitung:</i> Laptop, Browser, Headset mit Mikro, Kamera, stabiles Internet<br>
				<i>technische Aussattung Sessionleitung im Hörsaal:</i> Mikrophone, Zugang zur Onlineplattform Venueless
			</ul>				
		</li>

		<li><b>Welcome Desk</b> (bezahlt + gerne freiwillig)<br>
Der Welcome Desk ist die Servicestelle für alle Teilnehmer:innen. Beschreibung im Handbuch: https://www.fossgis.de/wiki/Handbuch#Ablauf_und_Organisation_des_Welcome_Desk
Hier läuft der Ticket-Checkin, Taschen, T-Shirts und Tagungsbände werden herausgegeben.
			<ul><i>Aufgaben:</i>
				<li>Informationen für Teilnehmende</li>
				<li>Checkin Teilnehmende - Ticket, Zertifikate sowie Ausgabe der Tasche, Namensschild, T-Shirt, Tagungsband</li>
				<li>Prüfung Workshopbuchung</li>
				<li>Verkauf von Tickets für Konferenz oder Workshop (Handkasse, Belege)</li>
				<li>ggf. Verkauf von T-Shirts, Tagungsbänden, usw.</li>
				<li>Garderobe</li>
			</ul>	
		</li>
		<li><b>Catering</b> (freiwillig oder bezahlt)<br>
Die Pausenversorgung für die Konferenzteilnehmenden ist wichtig und wird von einem Caterer erledigt. Da Caterer erfahrungsgemäß zu wenig Personal dabei haben, stellt die Konferenz Helfer:innen beim Catering zur Verfügung.
			<ul><i>Aufgaben:</i>
				<li>Lieferungen entgegennehmen</li>
				<li>Getränke vom Lager holen und ausreichend bereit halten</li>
				<li>Tische vorbereiten und Angebot anrichten</li>
				<li>falls nicht vom Caterer: Obst und Gemüse waschen und schnippeln</li>
				<li>Geschirr wegräumen</li>
			</ul>	
		</li>

		<li><b>Hörsaalhelfer</b> (bezahlt)<br>
In jedem Hörsaal gibt es eine verantwortliche Person.
			<ul><i>Aufgaben:</i>
				<li>Technik betreuen (im Vorfeld mit der Technik vertraut machen (Licht, Jalousien, Audio- und Videoanlage, Rechner)
				<li>morgens an jede Tür ein aktuelles Tagesprogramm hängen
				<li>Unterstützung der Vortragenden beim Laden ihrer Präsentationen auf den Präsentationsrechner
				<li>Kontrolle, ob Unterschrift zur Einverständniserklärung zur Aufzeichnung des Vortrages vorliegt. Wenn nicht, einholen
				<li>Versorgung der Vortragenden mit Wasser
				<li>Koordinierung der Platzierung der TN, damit nicht alle außen sitzen
				<li>Bei Fragen aus dem Publikum das Mikrofon herumtragen
				<li>Vortrag als PDF auf Laptop im Raum speichern
			</ul>	
		</li>
		<li><b>Workshophelfer</b> (bezahlt)<br>
In jedem Workshopraum gibt es eine verantwortliche Person.
			<ul><i>Aufgaben:</i>
				<li>Technik betreuen (im Vorfeld mit der Technik vertraut machen (Licht, Jalousien, Audio- und Videoanlage, Rechner)
				<li>morgens an jede Tür ein aktuelles Tagesprogramm hängen
				<li>Versorgung der Workshopleiter mit Wasser
				<li>Kontrolle/Checkin Worksshop-Teilnehmende
				<li>Feedbackbögen einsammeln
			</ul>
	</ul>

	<h4>Helfen Online umzusetzen</h4>
