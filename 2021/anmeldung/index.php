	<h3>Workshop buchen</h3>
	<p>Das Programm bietet die Möglichkeit einen oder mehrere Workshops zu buchen. Für jeden Workshop sind 90 Minuten vorgesehen, der Preis beträgt 120 EUR. Sie nehmen mit Ihrem eigenen Gerät am Workshop teil, den Zugang erhalten Sie über Ihren Ticketbuchungslink. Der Workshop ist interaktiv und wird in einer Videokonferenz stattfinden. Im Ticketshop ist im jeweiligen Workshop ein Stichwort zur Durchführungsart angegeben, dieses gibt an, welche Vorbereitungen Ihrerseits getroffen werden können, um erfolgreich teilzunehmen. 
		<ul>
		<li><i><b>Browserbasiert</b></i> heißt, Sie benötigen lediglich einen aktuellen Internet-Browser.</li>
		<li><i><b>OSGeoLive 14.0</b></i> heißt, im Workshop wird mit der aktuellen OSGeoLive gearbeitet, diese kann als Image von einem USB-Stick oder als Virtuelle Box auf Ihrem Computer laufen. Hier finden Sie eine <a href="http://live.osgeo.org/de/index.html"> Anleitung zur Installation der OSGeoLive.</a></li>
		<li><i><b>Individuell</b></i>, heißt sie erhalten in der Woche vor der Konferenz eine Mail mit detaillierten Informationen zum Workshop sowie die Möglichkeit in einem Termin Ihre Technik zu checken.</li>
		</ul>
  	</p>

	<h3>Bildungsurlaub</h3>
	<p>Die Anerkennung der FOSSGIS-Konferenz als Bildungsurlaub ist beantragt, zunächst für die Bundesländer in Deutschland, wo die Beantragung keine zusätzlichen Kosten verursacht (BE, BB, HB, HH, NI, RP, SL, ST und SH). Weitere können bei Bedarf beantragt werden. Teilnehmer:innen, die dieses Angebot nutzen wollen, beantragen dies bei beim Arbeitgeber. Informationen zum Bildungsurlaub finden Sie unter <a href="https://www.bildungsurlaub.de/home.html"> www.Bildungsurlaub.de</a>.</p>

	
	<h3>Netzwerkwelt</h3>

	<p>Um optimales Netzwerken während der Veranstaltung zu ermöglichen, wird es eine virtuelle Variante des OST Campus Rapperswil geben, wir nennen es die <i>Netzwerkwelt</i>.<br>
	   In der <i>Netzwerkwelt</i> können sich die Teilnehmenden in einer virtuellen Karte/Landschaft bewegen und einander begegnen. Zum Einstieg gibt man sich einen Namen und wählt einen Avatar aus. Nach der Landung im Eingangsbereich bewegen Sie sich mit Hilfe der Tastatur. Sie können die <i>Netzwerkwelt</i> erkunden (Informationsräume, Firmenausstellung betreten) und Sie sehen auch, ob weitere Teilnehmende unterwegs sind. Wenn Sie einer Person begegnen, öffnet sich ein Videochat, welcher sich mit einem Geräusch ankündigt, Sie können sich austauschen oder weglaufen - fast wie im echten Leben ;-).</p>

	<h3>Helfer</h3>
	<p>Freiwillige Helfer:innen sind eingeladen und willkommen vor der Konferenz bei der technischen Vorbereitung und während der Konferenz bei der technischen Betreuung oder als Sessionleitung zu unterstützen. Bei Interesse bitte im <a href="https://helfer.fossgis.de/" target="_blank">Helfersystem</a> [kommt noch] registrieren und eine E-Mail an das <a href="mailto:helfer@fossgis.de"> Helferteam</a> senden, damit wir Sie kontaktieren können. Helfer:innen dürfen das kostenfreie "Online-Konferenzticket für Helfende" buchen.
Bei Fragen empfehlen wir die <a href="https://pretix.eu/fossgis/2021/page/helfer/" target="_blank">FAQ</a> [kommt noch] zu lesen.</p>

