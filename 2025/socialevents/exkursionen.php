		<h3 id="MichaeliskircheBinnenalster" name="MichaeliskircheBinnenalster" >Stadtrundgang: Michaeliskirche – Elbtunnel – Elbphilharmonie – Speicherstadt – Karolinenviertel – Rathaus – Binnenalster</h3>
		<p>Samstag, 23.03.2024, Betreuung: Wolfgang Hinsch</p>
		<p>Wir treffen uns an der S-Bahn Haltestelle Stadthausbrücke, Bahnsteig, Aufgang zur Michaeliskirche (aus Richtung Hauptbahnhof vorne) um 9:15 Uhr (Abmarsch 9:30 Uhr). Wer das verpasst, kann die Gruppe an der Michaeliskirche (10:00 Uhr) noch einholen.<br>
		<p>Für diese Exkursion fallen Besichtigungsgebühren an, die von den Teilnehmern selbst zu tragen
sind, wenn sie an den Besichtigungen teilnehmen wollen. Denkt bitte an passendes Kleingeld oder Kartenzahlung.
		Eintrittsgelder (Stand: 12/23):
		<ul>
		<li>Michaeliskirche Turm: € 7,00</li>
		<li>Optional Mahnmal Nikolaikirche Turm: € 6,00</li>
		<li>Optional Rathausführung: € 7,00</li>
		<li>Optional Internationales Maritimes Museum: € 17,00</li>
		<li>Optional Auswanderermuseum € 13,00</li>
		</ul></p>
		Der Rundgang fängt an mit einer Turmbesteigung der St. Michaeliskirche, dem Wahrzeichen der Stadt und gleichzeitig dem mit 82 m zur Zeit höchsten öffentlich zugängigen Aussichtspunkt, von dem aus auch heute noch ein Trompeter um 10 Uhr einen Choral in alle 4 Himmelsrichtungen bläst. Die Turmbesteigung kann durch die Fahrt mit einem Aufzug etwas vereinfacht werden. <br>
		Der Weg führt weiter über den Aussichtspunkt Stintfang zum Alten Elbtunnel, den Landungsbrücken, einer kurzen Fahrt mit der Fähre zur Elbphilharmonie (auf den Besuch der Plaza wird aus Zeitgründen voraussichtlich verzichtet), weiter zu Fuß durch den Museumshafen, die Speicherstadt, vorbei an der Katharinenkirche, weiter durch die alten Gassen am Cremon und die Deichstraße, zum Mahnmal der Nikolaikirche, mit 147 m dritthöchster Kirchturm Deutschlands (weltweit Nr. 5). Im Turm gibt es in 76 Meter Höhe eine Aussichtsplattform, die mit einem Aufzug erreicht werden kann. Wir gehen weiter vorbei an der Börse und dem Rathaus,
durch die Alsterarkaden bis zur Binnenalster. <br>
		Das Ende wird grob auf 14:30 Uhr geschätzt, hängt aber stark davon ab, wie lange sich die Teilnehmenden an den jeweiligen Objekten aufhalten. Insbesondere sind Besuche der Aussichtsplattformen der Elbphilharmonie und des Nikolaiturms nicht eingeplant. Die eigentliche Strecke zu Fuß beträgt ca. 6,3 km + Fährfahrt.<br>
		Jeder Teilnehmer hat natürlich jederzeit die Möglichkeit, den Rundgang vorzeitig zu beenden und auf einem nahe gelegenen Bahnhof in U- oder S-Bahn zu steigen. Wer einen festen Zug für die Heimfahrt gebucht hat, achte bitte selbst auf die Zeit.</p>
		<p>Wer immer noch nicht genug hat, und falls die Zeit reicht, ist auch eine Führung durch das Rathaus möglich. Ein Teil der Räume im Erdgeschoss kann i.d.R. auch ohne Führung besichtigt werden. Abhängig von der politischen Lage (Staatsbesuch etc.) ist es nicht immer möglich, das Rathaus zu besichtigen.</p>
 		<p>Erforderlich ist eine 9-Uhr-Tageskarte für den Nahverkehr im HVV (Sa und So ganztägig gültig), Zonen AB, oder das 49-Euro-Ticket und dem Wetter angepasste Kleidung sowie eine gewisse Treppengängigkeit.<br>
		Die Exkursion findet im Prinzip bei jedem Wetter statt, aber im Falle von Monsun beschränken wir uns auf möglichst kurze Wege und Sehenswürdigkeiten, die von innen besichtigt werden können. Treffpunkt und Uhrzeit bleiben unverändert, das Programm erstreckt sich in dem Fall auf Michaeliskirche, Alten Elbtunnel, und alternativ Internationales Maritimes Museum oder Auswanderermuseum.</p>
		<p>Die Neigung der Rampen vom Ufer auf die Anleger der Schiffe ist tide- und wetterabhängig. Sie reicht von fast waagerecht bis steil und kann rutschig sein. Die Wege sind teilweise Treppen. Dieser Rundgang ist nicht barrierefrei.</p>
			
