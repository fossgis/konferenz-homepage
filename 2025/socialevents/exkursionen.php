		<p>Wir treffen uns am Hamburger Hauptbahnhof, S-Bahnsteig Gleis 1 (Nebentunnel), ganz vorn (in Fahrtrichtung) um 14:45, Abfahrt ist um 15:08 mit der S1 bis Blankenese. Dort wandern wir durch das Treppenviertel zum Anleger, setzen mit der Fähre über nach Cranz (Altes Land), fahren mit dem Bus nach Finkenwerder und von dort mit der Fähre bis zu den Landungsbrücken, wo wir
gegen 18:30 eintreffen sollten.</p> 
		<p>Erforderlich ist eine 9-Uhr-Tageskarte für den Nahverkehr im HVV, Zonen AB, oder das 49-EuroTicket, dem Wetter angepasste Kleidung sowie eine gewisse Trittsicherheit auf langen, teilweise etwas unebenen und unregelmäßigen historisch entstandenen Treppenwegen.<br>
