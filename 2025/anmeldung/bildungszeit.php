					<h3 id="Bildungszeit" name="Bildungszeit">Was ist Bildungszeit?</h3>
			<p>Bildungszeit, umgangssprachlich auch Bildungsurlaub ist der gesetzlich verbriefte Anspruch auf Weiterbildung für Arbeitnehmer:innen im Rahmen der Arbeitszeit. Mit der Bildungszeit fördert der Staat die Initiative von Arbeitnehmer:innen zum lebenslangen Lernen.</p>
			<p>Weiterbildung ist ständig notwendig, da Wissen veraltet oder seine Gültigkeit oder Bedeutsamkeit verlieren kann, neues oder aufbauendes Wissen ist notwendig. Die digitalisierte Arbeitswelt fordert immer wieder neue technische, organisatorische oder kommunikative Kompetenzen.</p>
			<p>Eine Anerkennung als Bildungszeit gibt Arbeitnehmenden das Recht, die Veranstaltung während der Arbeitszeit zu besuchen und sich dafür freistellen zu lassen.<br>
Der Mehrwert besteht darin, dass der Arbeitnehmer:in pro Jahr bis zu 5 Tage Bildungszeit zustehen, unter Weiterzahlung des Gehalts.</p>
			<p>Dass heißt, es ist nicht notwendig für eine Teilnahme an der FOSSGIS-Konferenz den Erholungsurlaub zu beanspruchen, sondern die Konferenz kann als Weiterbildungsveranstaltung genutzt werden.</p>
					
			<h3>Wie läuft die Beantragung beim Arbeitgeber?</h3>
			<p>Es ist jeweils das Bundesland zuständig, wo sich der Arbeitsort befindet. Die genauen Regelungen zu jedem Bundesland sind in jeweiligen Landesgesetzen geregelt. Nähere Erläuterungen sind auf der Website <a href="https://www.bildungsurlaub.de/ "><i>Bildungsurlaub.de</i></a> zu finden.</p>

			<p>Reichen Sie bei Ihrem Arbeitgeber, am besten dort, wo Sie auch Ihren Antrag auf Erholungsurlaub anmelden, folgende Unterlagen ein:
			<ul>
			<li>Formloses Schreiben: “Hiermit mache ich meinen Anspruch auf Freistellung für Bildungszeit vom 26.03.bis 29.03.2025 geltend. Beigefügt sind Ablaufplan, Anerkennungsbescheid und Anmeldebestätigung”.</li>
			</ul></p>
			<p>Den inhaltlichen Ablaufplan finden Sie ab Ende des Jahres im <a href="https://fossgis-konferenz.de/2025/programm/">Programm</a>. Für die notwendige Anmeldebestätigung nutzen Sie entweder die Bestell-Email oder Ihre Rechnung, die Sie bei Konferenzticketbuchung erhalten. Anerkennungsbescheid siehe jeweiliges Bundesland.</p>
<p>Nach dem Bildungsurlaub: Sie laden sich Ihre Teilnahmebescheingung, welche wir Ihnen in Ihrer Bestellung zur Verfügung stellen herunter und geben diese bei Ihrem Arbeitgeber ab.</p>

<p>Für einige Länder gibt es Vordrucke für die Beantragung und Abrechnung beim Arbeitgeber. Hierzu informieren Sie sich bitte unter <a href="https://www.bildungsurlaub.de/ ">https://www.bildungsurlaub.de/</a>.</p>

				
