  	<h2>Einreichungstypen</h2>

	<p>Das Konferenzprogramm beinhaltet unterschiedliche Formate.</p>
	<ul>
		<li><strong>Lightning Talk</strong><br>
Ein Lightning Talk bietet Gelegenheit neue Software, neue Projekte oder neue Ideen in 5 Minuten zu präsentieren. Es ist möglich, noch kurz vor und während der Konferenz einen Lightning Talk einzureichen, sofern Kapazitäten im Programm verfügbar sind.</li>

		<li><strong>Vortrag</strong><br>
Vorträge bilden den Hauptteil der Konferenz. Ein Vortrag ist 20 Minuten lang. Danach sind 5 Minuten Zeit für Fragen.</li>
		
		<li><strong> Vortrag mit Paper im Academic Track</strong><br> 
Begutachtung im Peer-Review-Verfahren durch das Academic Track-Komitee mit dem Ziel einer zitierfähigen Veröffentlichung eines Full Papers (6-8 Seiten).    
Der einzureichende Abstract sollte ca. 3.000 Zeichen umfassen.</li>

		<li><strong>Demo-Session</strong><br>
Die Demo-Session dient der Vorführung von Software oder Prozessen („Live-Hacking“) und ist 45 Minuten lang.</li>

		<li><strong>Workshop</strong><br>
Workshops finden von Mittwoch bis Freitag parallel zu den Vorträgen statt. Die Teilnahme am 90-minütigen Workshop ist kostenpflichtig, die Zahl der Teilnehmenden ist begrenzt. Sollte der Workshop stark nachgefragt sein, sind mehrere Personen für die Betreuung vorgesehen.</li>

		<li><strong>Anwendertreffen/BoF</strong><br>
Anwendertreffen oder „Birds of a Feather“-Sessions (BoF) ermöglichen es Gleichgesinnten und -interessierten sich konzentriert und selbstorganisiert zu einem bestimmten Thema in einer 60-minütigen Session auszutauschen.</li>

		<li><strong>Expert:innenfragestunde</strong><br>
Die Idee des Formats ist, dass Expert:innen für 60 Minuten für Fragen von Teilnehmenden zur Verfügung stehen.</li>

		<li><strong>Poster-Session</strong><br>
Die Poster-Session bietet eine Plattform, um innovative Ideen, laufende (Forschungs-)Projekte und Studienarbeiten in einem interaktiven Format vorzustellen. Das Posterformat kann dabei zwischen A0 bis A2 gewählt werden. Zusätzlich besteht die Option, einen Kurzbeitrag (max. 3 Seiten) einzureichen, der bei Annahme im Tagungsband veröffentlicht wird. Sollte die Poster-Session nicht realisierbar sein, so besteht die Möglichkeit stattdessen einen Lightning-Talk zu halten.</li>

		<li><strong>Sonstiges</strong><br>
Ihre Einreichung passt nicht zu einem der Einreichungstypen? Dann setzen Sie sich bitte vor dem Ende der Einreichungsfrist mit dem Programm-Komitee per <a href="mailto:programm@fossgis.de? subject=Frage%an%das%Programmkomitee"> per E-Mail</a> in Verbindung.</li>
	</ul>


	<h2>Bewertungskriterien</h2>

	<p>Bei der Bewertung der Einreichungen finden folgende Kriterien Anwendung.</p>
	<ul>
	<li>Die Inhalte des Beitrags dienen der Wissensvermittlung und dem Erfahrungsaustausch bezüglich aktueller Themen.</li>

	<li>Bei der Einreichung muss es um freie Software oder freie Daten gehen.</li>
		<ul>
		<li><a href="https://www.gnu.org/licenses/license-list.html">Software ist frei</a>, wenn ihre Lizenzbedingungen von der Free Software Foundation als frei anerkannt werden und der Quellcode öffentlich verfügbar ist.</li>
		<li>Daten sind frei, wenn ihre Lizenzbedingungen der <a href="https://opendefinition.org/">Open Definition</a> entsprechen.</li>
		<li>Der FOSSGIS e.V. unterstützt die Initiative <a href="https://publiccode.eu/de/">Public Money, Public Code</a>. Wir ermuntern alle Projekte und Organisationen, die mit öffentlichen Mitteln freie Software finanzieren, sich und ihre Projekte / Software vorzustellen und von ihren Erfahrungen zu berichten.</li>
		<li>Selbstverständlich freuen wir uns über Beiträge, die die Migration von unfreier zu freier Software oder die Nutzung proprietärer Formate/Schnittstellen mit freier Software thematisieren.</li>
				</ul>
	<li>Vorträge sollten einen Neuigkeitswert bieten und aufzeigen, inwieweit das Thema die Verbreitung von Open Source unterstützt (beispielsweise durch Projektverantwortung (Maintainer), Unterstützung (Contributor), Einsatz/Anwendung (User)).</li>
	<li>Die Einreichung wird nach inhaltlichen Kriterien bewertet. Daher ist für die Bewertung eine aussagekräftige Beschreibung (die etwas mehr Information als die Zusammenfassung enthält) wichtig.</li>
	<li>Eine Einreichung muss von einer oder mehreren natürlichen Personen erfolgen, die den Vortrag / Workshop dann auch halten.</li>
	<li>Beiträge von Sponsoren werden nicht bevorzugt behandelt, die Vorträge dienen nicht der reinen Firmenpräsentation.</li>
	<li>Ausschließliche Werbebeiträge sind unerwünscht.</li>
	<li>Das Ergebnis des Community-Votings wird vom Programmkomitee als Stimmungsbild aus der Community einbezogen.</li>

		<h3>Zusätzlich für Academic Track</h3>
		<p>Einreichungen – bzw. die Full-Paper – im Academic-Track werden zusätzlich nach gängigen wissenschaftlichen Kriterien _single blind_ bewertet. </p>
			
		<h3>Zusätzlich für Workshops:</h3>
		<p>Wir bevorzugen Workshops zu aktuellen und nachgefragten Themen. Die geschätzte Nachfrage auf Basis der Vorjahre hat einen erheblichen Einfluss auf die Auswahl.</p>

	<h2>Grundsätze der Programmgestaltung</h2>

	<p>Das Programmkomitee wünscht sich in jeder Hinsicht Diversität unter den Vortragenden. Neue Vortragende sind willkommen! Daher freuen wir uns besonders über Einreichungen von Personen, die noch nicht oder noch nicht oft vorgetragen haben. Sollten Sie unsicher sein, zögern Sie nicht das Programm-Komitee zu kontaktieren (<a href="mailto:programm@fossgis.de? subject=Fragen_ans_Programmkomitee">per E-Mail</a>, Telefon: +49 30-62932037)</p>

	<p>Das Programmkomitee ist bestrebt ein ausgewogenes und informatives Programm anzubieten und achtet auf Ausgewogenheit bezüglich Vortragenden und Firmen.</p>

	<p>Die Beiträge werden ausschließlich vor Ort präsentiert. Die Konferenzsprache ist Deutsch. In Ausnahmefällen können Beiträge auf Englisch erfolgen. Falls Themen doppelt eingereicht werden, bemüht sich das Programmkomitee, gemeinsam mit den Einreichenden eine Lösung zu finden.</p>

	<p>Es ist geplant, die Vorträge live zu streamen sowie die Formate Anwendertreffen/BoF und Expert:innenfragestunde hybrid zu gestalten. Aus diesem Grund ist es erforderlich, dass alle Vortragenden dem Streaming und der Aufzeichnung ihrer Beiträge zustimmen.</p>

