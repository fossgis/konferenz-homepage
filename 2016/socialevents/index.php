                <h2>Social Events</h2>

                <h3>Sonntag, 03. Juli 2016</h3>
				<h4>OSM-Sonntag.</h4>
				<p>Am Vortag der FOSSGIS-Konferenz findet der OSM-Sonntag statt und ist als eine Mischung aus Konferenz, Hackweekend und Mappertreffen gedacht. Es richtet sich an Mapper, Entwickler und OSM-Interessierte.</p>

                <p>Es gibt Vorträge und Workshops die jeweils eine länger von 60 Minuten eingeplant sind. So möchten wir ermöglichen tiefer in die Thematik einzusteigen, wie z.B. auf einer FOSSGIS-Konferenz üblich. Auch kann man so länger über ein kontroverses Thema diskutieren.</p>

                <p>Parallel zu den Vorträgen/Workshops stehen Räumlichkeiten zur Verfügung in denen ihr euch mit anderen Teilnehmern austauschen und an euren Projekten (z.B. Entwicklerprojekte oder Editieren mit JOSM/iD/…) arbeiten könnt. </p>

                <p>Weitere Informationen dazu sowie die Planung finden Sie im <a href="https://wiki.openstreetmap.org/wiki/FOSSGIS_2016/Hackday_t-1">OSM-Wiki</a>. Wer sich einbringen möchte, trägt sich bitte ein. Fragen dazu gerne an das <a href="mailto:konferenz-orga@fossgis.de">Konferenzorganisationsteam</a>.

				<h4>Inoffizieller Start</h4>
                <p>Ab 18.30 Uhr gemeinsames Abendessen für alle schon anwesenden FOSSGISler im <a href="http://taste-gassner.com/stieglkeller/">Stieglkeller Salzburg</a> (Festungsgasse 10, 5020 Salzburg). Wir treffen uns bei schönem Wetter auf der Terasse des Stieglkellers, für die Schlechtwettervariante steht ein Raum zur Verfügung. Wir werden vom <a href="https://www.fossgis.de/w/images/e/ea/Schmankerlbuffet_2016.pdf">Schmankerlbuffet</a> essen, Getränke gibt es a la carte. Jeder bezahlt seine Rechnung selbst. Das EM-Spiel um 21 Uhr wird auch im Stieglkeller übertragen, es ist ein Tisch für 10-12 Personen reserviert.</p>

				<h3>Montag, 04. Juli 2016</h3>
				<h4>Eröffnung FOSSGIS-Konferenz</h4>
				<p>um 13:00 Uhr im Hörsaal Audimax.</p>
				<h4>Anwendertreffen</h4>
                <p>Es können Anwendertreffen, Sprints und BOFs selber organisiert werden. Räume stehen zur Verfügung, bitte <a href="mailto:konferenz-orga@fossgis.de">per E-Mail</a>  an das Konferenzteam anfragen oder das Team vor Ort ansprechen.</p>

				<h3>Dienstag, 05. Juli 2016</h3>
				<h4>Abendveranstaltung "Dialoge am Amphitheater"</h4>
				<p>das gesellige Beisammensein findet von 18 - 23 Uhr zusammen mit dem "Icebreaker" der AGIT auf der Terasse des Gebäudes Naturwissenschaften (Nawi) in der Universität Salzburg statt. Die "Dialoge am Amphitheater" werden sich durch ein offenes Ambiente auszeichnen. Der Eintritt ist im FOSSGIS-Konferenz-Ticket enthalten. Wir bitten darum bei der Teilnahmeanmeldung ja oder nein anzugeben, dies dient der guten Planung der Veranstaltung. Als Schlechtwettervariante steht das Foyer der Universität zur Verfügung.</p>

                <h3>Mittwoch, 06. Juli 2016</h3>
                <h4>Sektempfang am FOSSGIS-Stand</h4>
                <p>Alle Mitglieder, Freunde und Interessierte des FOSSGIS-Vereins sind nach der Abschlussveranstaltung herzlich zum Sektausklang am FOSSGIS-Stand eingeladen.</p>

