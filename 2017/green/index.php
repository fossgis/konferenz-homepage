                <h2>FOSSGIS - Green Meeting</h2>
                <p>In 2016 war die FOSSGIS im Rahmen der AGIT "Green Meeting" zertifiziert. Auch wenn wir die Zertifizierung in diesem Jahr nicht verfolgen, wollen wir uns an die Standards halten.</p>
				<p>Es ist unsere „grüne Vision“ die FOSSGIS-Konferenz nach den Prinzipien der Umweltverträglichkeit und Nachhaltigkeit auszurichten. Wir wollen mit unserem Handeln zu einer Welt beitragen, in der wir verantwortungsvoll mit unseren Ökosystem, ihren Rohstoffen und den Menschen umgehen. Leisten auch Sie Ihren aktiven Beitrag zum Umweltschutz, beachten Sie unsere Empfehlungen!</p>
				<p><b>Green Location</b></p>
				<p>Die Gebäude Audimax (AM) und Informatik-Mathematik (IM) der Universität Passau haben einen direkten Grünflächenzugang und liegen einzigeartig am Inn. Die Innpromenade lädt zu einem Spaziergang ein. Die Teilnehmer und Aussteller der FOSSGIS erleben ein „Event im Grünen“.</p>
				<p>Die Universität Passau ist durch seine zentrale Lage und die hervorragende Anbindung an öffentliche Verkehrsmittel sehr gut zu erreichen.</p>
				<p><b>Travel Green</b></p>
<p>Mit Bahn reisen Sie besonders umweltfreundlich nach Passau. Der Nahverkehr bietet den Besuchern gute Verbindungen von der Altstadt und dem Hauptbahnhof zum Veranstaltungsort.</p>
				<p><i>Empfehlung</i>: Viele Hotels und Pensionen liegen im unmittelbaren Umfeld des Veranstaltungsortes. Die Universität Passau können Sie bequem zu Fuß von vielen Quartieren aus erreichen.</p>

				<p><b>Act Green</b></p>
				<p>Bitte geben Sie am Ende der Konferenz ihre Badges und Lanyards am Welcome Desk zurück.</p>
