                <h2>Social Events</h2>
				
				 <h3>Dienstag, 21. März 2017</h3> 
                <p>Ab 19:30 Uhr Inoffizieller Start mit einem gemeinsamen Abendessen für alle schon anwesenden FOSSGISler in der <a href="http://www.padu-innstrasse.de/">Pizzeria Padu</a> (Innstraße 46, 94032 Passau) im Nebenraum. Warme Küche gibt's bis 22:30. Jeder bezahlt seine Rechnung selbst. </p>

                <h3>Mittwoch, 22. März 2017</h3>
				<h4>Eröffnung FOSSGIS-Konferenz</h4>
				<p>um 13:00 Uhr im Hörsaal Audimax.</p>
				<h4>Anwendertreffen</h4>
                <p>Es können Anwendertreffen, Sprints und BOFs selber organisiert werden. Räume stehen bis 21 Uhr zur Verfügung, bitte <a href="mailto:konferenz-orga@fossgis.de">per E-Mail</a>  an das Konferenzteam anfragen oder das Team vor Ort ansprechen.</p>
				<h4>Abendveranstaltung "Dialoge am Inn"</h4>
				<p>Das gesellige Beisammensein am Inn findet von 19 - 23 Uhr in der Mensa der Universität Passau statt. Die "Dialoge am Inn" werden sich durch ein offenes Ambiente auszeichnen. Der Eintritt ist im FOSSGIS-Konferenz-Ticket enthalten. Wir bitten darum bei der Teilnahmeanmeldung ja oder nein anzugeben, dies dient der guten Planung der Veranstaltung.</p>
				
				<h3>Donnerstag, 23. März 2017</h3>
				<h4>FOSSGIS e.V. Mitgliederversammlung</h4>
				<p>um 18:00 Uhr im Seminarraum IM 008. Es wird für alle Pizza bestellt.</p>
				
				<h4>Anwendertreffen</h4>
                <p>Es können Anwendertreffen, Sprints und BOFs selber organisiert werden. Räume stehen bis 21 Uhr zur Verfügung, bitte <a href="mailto:konferenz-orga@fossgis.de">per E-Mail</a>  an das Konferenzteam anfragen oder das Team vor Ort ansprechen.</p>
				
				<h3>Freitag, 24. März 2017</h3>
				<h4>Sektempfang am FOSSGIS-Stand </h4>
				<p>Alle Mitglieder des FOSSGIS-Vereins, Freunde und Interessierte sind herzlich ab 14:30 Uhr zum Sektausklang eingeladen.</p>
				
				<h4>OSM-Mapping-Event</h4>
				<p>Unter der Bezeichnung Qualitätsoffensive OSM Mapping Passau werden sich Mapper der Altstadt Passau widmen und Themen wie Geschäfte/Öffnungszeiten/Ärzte/Rechtsanwälte, 3DTagging, Barrierefreiheit, Briefkästen, Indoor (Aufzüge) bearbeiten. Falls Sie soetwas noch nie gemacht haben, ist das die Gelegenheit für den Einstieg. Das Mapping-Event soll auch für Anfänger geeignet sein, deshalb findet um 16 Uhr ein kurzer Workshop statt, in dem das How-To-Do besprochen wird. Anmeldung zur Veranstaltung entweder vor Ort oder im <a href="https://wiki.openstreetmap.org/wiki/FOSSGIS_2017/OSM-Events"> OSM-Wiki </a>. Das Mapping-Event ist bis ca. 20 Uhr geplant.</p>

                <h3>Samstag, 25. März 2017</h3>
				<h4>OSM-Samstag </h4>
				<p>Von 9.30 bis 18 Uhr findet eine OSM-Unconference statt. Interessierte sind eingeladen daran teilzunehmen. Die Anmeldung und Themensammlung erfolgt im <a href="https://wiki.openstreetmap.org/wiki/FOSSGIS_2017/OSM-Events"> OSM-Wiki </a> sowie an der Pinnwand am FOSSGIS-Stand. Um 10 Uhr werden die Themen in Zeiten und Räume sortiert. Zu Mittag gibt es Pizza.</p>

